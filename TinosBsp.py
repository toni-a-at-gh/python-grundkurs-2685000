a = 1
b = 4
c = 6
# Zahlen ausgeben:
print("a = ", a, "; b = ", b, "; c = ", c)

#Addition:
print ("a + b + c = ", a+b+c)

#Subtraktion:
print ("a - b - c =", a-b-c)

#Division:
print ("a / (b + c) = ", a/(b+c))

#Multiplikation:
print ("a * b * c = ", a*b*c)

#Modulo:
print("Rest von a * b / c = ", (a*b)%c)
